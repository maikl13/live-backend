<?php
// قم بتغيير 'Your-Header-Name' إلى الاسم الفعلي لرأس الطلب المخصص
$uid = 'uid';

// تحقق مما إذا كان رأس الطلب المخصص محددًا في الطلب
if (isset($_SERVER['HTTP_' . $uid])) {
    // استرجاع قيمة رأس الطلب المخصص
    $uid = $_SERVER['HTTP_' . $uid];

    // استخدم قيمة رأس الطلب المخصص حسب الحاجة
    echo "قيمة رأس الطلب المخصصة: $uid";
} else {
    echo "لم يتم العثور على رأس الطلب المخصص '$uid' في الطلب.";
}
?>
